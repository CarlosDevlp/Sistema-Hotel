    void buscar(){
        
           DefaultTableModel modelo = (DefaultTableModel) tablaListaProd.getModel();
           if(txtTermProd1.getText()!=null && txtcati.getText()!=null){
            try{
            ArrayList<Map<String,String>> resultadoProductos = BasicDao.select("Producto", new String[]{"*"}, "NombrePro like '%"+txtTermProd1.getText().toString()+"%'"); 
            
            for(int i = 0; i<resultadoProductos.size() ; i++){
                Object datos[]=new Object[3];
                Map<String,String> Producto = resultadoProductos.get(i);
                modelo.addRow(new Object[]{
                    Producto.get("idProducto"),
                    Producto.get("NombrePro"),
                    Producto.get("PrecioPro")
                });
            }  
             }catch(Exception e){
                 ArrayList<Map<String,String>> resultadoProductos = BasicDao.select("Producto", new String[]{"*"}, "NombrePro like '%"+txtcati.getText().toString()+"%'"); 
            
            for(int i = 0; i<resultadoProductos.size() ; i++){
                Object datos[]=new Object[3];
                Map<String,String> Producto = resultadoProductos.get(i);
                modelo.addRow(new Object[]{
                    Producto.get("idProducto"),
                    Producto.get("NombrePro"),
                    Producto.get("PrecioPro")
                });
            } 
             }
            }else{
               JOptionPane.showMessageDialog(null, "Producto no Encontrado");
           }
              
    }
    
