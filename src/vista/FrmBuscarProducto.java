    private void btnAgregarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarProductoActionPerformed
        
        
        
        DefaultTableModel modelo = (DefaultTableModel) FrmServicioHabitacion.tablaProductos.getModel();
        DefaultTableModel modeloInterno = (DefaultTableModel) tablaListaProd.getModel();
        modelo.addRow(new Object[]{
            servHab.txtDNI.getText(),
            servHab.txtFullName.getText(),
            servHab.txtHabitacion.getText(),
            servHab.txtFechaActual.getText(),
            modeloInterno.getValueAt(tablaListaProd.getSelectedRow(), 1),
            txtCantidad.getText(),
            modeloInterno.getValueAt(tablaListaProd.getSelectedRow(), 2),
            ""+(
                    Integer.parseInt(txtCantidad.getText()) * Double.parseDouble(modeloInterno.getValueAt(tablaListaProd.getSelectedRow(), 2).toString())
               )
        });
        montoAcumulado += Integer.parseInt(txtCantidad.getText()) * Double.parseDouble(modeloInterno.getValueAt(tablaListaProd.getSelectedRow(), 2).toString());
        this.servHab.MontoAcumuladoTotal += montoAcumulado;
        this.servHab.txtTotalPagar.setText(""+this.servHab.MontoAcumuladoTotal);
        //this.servHab.setVisible(true);
        this.setVisible(false);
        
    }//GEN-LAST:event_btnAgregarProductoActionPerformed

